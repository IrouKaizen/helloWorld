public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("HelloWorld");
    }
}

/* Explication :
- Le programme crée une classe `HelloWorld`, qui est une convention pour définir les programmes Java.
- La méthode `main` est le point d'entrée de tout programme Java. C'est là que l'exécution commence.
- `System.out.println` est utilisé pour afficher le texte "HelloWorld" sur la console.

Pour exécuter ce programme :
1. Enregistrez-le dans un fichier nommé `HelloWorld.java`.
2. Compilez-le avec la commande `javac HelloWorld.java`.
3. Exécutez-le avec la commande `java HelloWorld`.

Cela affichera `HelloWorld` dans la console. */
